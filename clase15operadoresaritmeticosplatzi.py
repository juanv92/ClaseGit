#operadores aritmeticos

x = 5
y = 10

#Suma + 
print("Suma", x + y)

#Resta - 
print("Resta", x - y)

#Multiplicacio *
print("Multiplicacion", x * y)

#Division /
print("Division", x / y)

#modulo %
print("Modulo(resto)", y % x)

#potencia **
print("Potencia", y ** x)

#Division entera //
print("Division Entera", y // x)

par = 7
print(par % 2 ==0)

