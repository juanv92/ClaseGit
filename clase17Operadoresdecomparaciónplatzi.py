#Operadores de comparación

x = 5

y = 3 

z = 5

print(x==y) #¿si es igual? False

print(x!=y) #¿si es diferente? True

print(x>y)  #¿si es mayor? True

print(x<y)  #¿si es menor? False

print(x>=y) #¿si es mayor o igual? True

print(x>z) 

print(x<=z) #¿si es menor o igual? True 

#operadores logicos

x > y  and y > z #AND (y) ambas condiciones deben ser verdaderas

print(x > y and y > z) 

print(x > y or y >z ) #OR (o) al menos una condición debe ser verdadera

v = True

print(not v) #NOT (no) invierte el valor de verdad

f = False 

print(not f)
